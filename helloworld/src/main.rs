// Arthematic operations



fn main() {
    let a = 10;
    let b = 5;

    // Addition
    let sum = a + b;
    println!("Addition: {} + {} = {}", a, b, sum);

    // Subtraction
    let difference = a - b;
    println!("Subtraction: {} - {} = {}", a, b, difference);

    // Multiplication
    let product = a * b;
    println!("Multiplication: {} * {} = {}", a, b, product);

    // Division
    let quotient = a / b; // Integer division
    println!("Division: {} / {} = {}", a, b, quotient);

    // Remainder (Modulo)
    let remainder = a % b;
    println!("Remainder: {} % {} = {}", a, b, remainder);
}






